function mapCharacters(text: string, charMap: Record<string, string>): string {
  return text.split('').map(char => charMap[char] || char).join('')
}

function convertToPinyin(text: string): string {
  return text.split('').map(char => {
    const pinyin = pinyinMap[char]
    return pinyin ? `${char}(${pinyin})` : char
  }).join('')
}

function convertToReverse(text: string): string {
  const mapped = mapCharacters(text, reverseMap)
  return mapped.split('').reverse().join('')
}

export const fontConverter = {
  convert(text: string, style: FontStyle): string {
    if (!text) return ''
    
    switch (style) {
      case 'bold':
        return mapCharacters(text, boldMap)
      case 'italic':
        return mapCharacters(text, italicMap)
      case 'boldItalic':
        return mapCharacters(text, boldItalicMap)
      case 'outline':
        return mapCharacters(text, outlineMap)
      case 'script':
        return mapCharacters(text, scriptMap)
      case 'gothic':
        return mapCharacters(text, gothicMap)
      case 'reverse':
        return convertToReverse(text)
      case 'pinyin':
        return convertToPinyin(text)
      case 'symbol':
        return mapCharacters(text, gothicMap)
      default:
        return text
    }
  },

  convertAll(text: string): { style: FontStyle; name: string; result: string }[] {
    const styles: { style: FontStyle; name: string }[] = [
      { style: 'bold', name: '粗体' },
      { style: 'italic', name: '斜体' },
      { style: 'boldItalic', name: '粗斜体' },
      { style: 'outline', name: '空心字' },
      { style: 'script', name: '手写体' },
      { style: 'gothic', name: '哥特体' },
      { style: 'reverse', name: '颠倒文字' },
      { style: 'pinyin', name: '拼音标注' }
    ]

    return styles.map(s => ({
      ...s,
      result: this.convert(text, s.style)
    })).filter(s => s.result !== text)
  }
}
